package with_ide;

public class If_elseif {
    public static void main(String[] args) {
        int mark = 80;
        if (mark<40){
            System.out.println("fail");
        }else if(mark >=40 && mark <=80){
            System.out.println("pass");
        }else {
            System.out.println("D");
        }
    }
}
